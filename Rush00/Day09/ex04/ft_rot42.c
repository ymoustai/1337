char	*ft_rot42(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = ((str[i] + 42 - 'a') % 26 + 'a');
		}
		else if (str[i] >= 'A' && str[i] >= 'Z')
		{
			str[i] = ((str[i] + 42 - 'A') % 26 + 'A');
		}
		i++;
	}
	return (str);
}
