int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int		i;
	char	lol;

	i = 0;
	while (*(str + i) != '\0')
	{
		lol = *(str + i);
		ft_putchar(lol);
		i++;
	}
}
